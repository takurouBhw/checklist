    public function login(Request $request) {

        $user = User::where('email', '=', $request->email);

        // ユーザーの存在チェック
        if(is_null($user)) {
            return response()->json([
                'error' => 'emailが間違っています。'
            ], 401);
        }

        return response()->json([
            'user_id' => time(),
            'name' => $user->name,
            'error' => '',
        ], 200);
    }
